    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    String kullaniciAdi = jTextField1.getText();
    String sifre = new String(jPasswordField2.getPassword());
    Admin admin = new Admin();

    if (admin.adminLogin(kullaniciAdi, sifre)) {
        if (!isOpen) {
            JOptionPane.showMessageDialog(this, "Giriş başarılı", "Başarılı", JOptionPane.INFORMATION_MESSAGE);
            isOpen = true;
            AdminPanel adminPanel = new AdminPanel();
            adminPanel.addWindowListener(new java.awt.event.WindowAdapter() {
                @Override
                public void windowClosed(java.awt.event.WindowEvent windowEvent) {
                    isOpen = false;
                }
            });
            adminPanel.setVisible(true);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Hatalı giriş", "Hata", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton3ActionPerformed

